//FOR, WHILE

//Utilizar o for quando se sabe o intervalo definido
for (var i = 0; i <= 100; i++) {
  console.log(i);
}

//Usar o while quando não sabemos quantas vezes a repetição ocorrerá
var j = 0;
while (j <= 100) {
  console.log(j);

  j++;
}

var k = 2364167;
while (k > 50) {
  console.log(k);

  k /= 5;
}
